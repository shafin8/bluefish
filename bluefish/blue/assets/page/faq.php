<h1 >
1.  What is aquaculture?</h1>
<p>The broad term “aquaculture” refers to the breeding, rearing, and harvesting of animals and plants in all types of water environments including ponds, rivers, lakes, and the ocean. Aquaculture is used for producing seafood for human consumption; enhancing wild fish, shellfish, and plant stocks for harvest; restoring threatened and endangered aquatic species; rebuilding ecologically-important shellfish habitat; producing nutritional and industrial compounds; and providing fish for aquariums.
</p>

<h1>2. What is BLUE FISH HATCHERY's role in aquaculture?</h1>
<p>BLUE FISH HATCHERY has a multi-faceted role in aquaculture from supporting cutting-edge science and research to federal policymaking and regulation. The BLUE FISH HATCHERY Fisheries Office of Aquaculture fosters sustainable aquaculture that provides safe, sustainable seafood; creates employment and business opportunities in coastal communities; and complements BLUE FISH HATCHERY’s comprehensive strategy for maintaining healthy and productive marine populations, ecosystems, and vibrant coastal communities. BLUE FISH HATCHERY's aquaculture efforts are led by BLUE FISH HATCHERY Fisheries and include activities at BLUE FISH HATCHERY Fisheries science centers and regional offices, BLUE FISH HATCHERY's National Sea Grant program, and BLUE FISH HATCHERY's National Ocean Service. The Aquaculture Office integrates and coordinates the agency’s aquaculture policies, research, outreach, and international efforts.</p>

<h1>3. Why do we need aquaculture?</h1>
<p>Aquaculture serves many purposes and seafood production is paramount among them. Aquaculture has helped improve nutrition and food security in many parts of the world.  Increasing global population coupled with icreased per capita seafood consumption result in constant, growing demand for seafood. Global seafood consumption reached 143 million metric tons in 2009, which is an increase of more than 20 million tons in 10 years. According the the United Nations Food & Agriculture Organization, “With capture fisheries production stagnating, major increases in fish food production are forecast to come from aquaculture. Taking into account the population forecast, an additional 27 million tonnes of production will be needed to maintain the present level of per capita consumption in 2030.” Aquaculture is one of the most resource-efficient ways to produce protein. Fish come out well because, in general, they convert more of the food they eat into body mass than land animals. “Feed Conversion Ratios” indicate how many pounds of feed it takes to produce a pound of protein. As can be seen in the table below, salmon – the most feed-intensive farmed fish – is still far more efficient than other forms of protein production. Protein Feed Conversion Ratio Salmon 1.2 It takes 1.2 pounds of feed to produce 1 pound of salmon Beef 8.7 It takes 8.7 pounds of feed to produce 1 pound of beef Pork 5.9 It takes 5.9 pounds of feed to produce 1 pound of pork Chicken 1.9 It takes 1.9 pounds of feed to produce 1 pound of chicken</p>

<h1>4. How much seafood is produced by aquaculture?</h1>

<p>Globally, aquaculture supplies more that 50 percent of all seafood produced for human consumption – that percentage has been and will continue to rise. Conventional wisdom holds that traditional fisheries are producing near their maximum capacity and that future increases in seafood production must come largely from aquaculture. Experts at the United Nations Food and Agriculture Organization say we will need another 40 million tons of seafood worldwide per year by 2030 just to meet current consumption rates.</p>
<h1>5. Are U.S. farm-raised fish and shellfish safe?</h1>

<p>Yes, the U.S. laws governing the harvest and processing of seafood for human consumption are among the most stringent in the world. The responsibilities of monitoring and controlling seafood safety are divided among various agencies of the federal government and individual states. The primary federal agencies involved with seafood safety include: Food and Drug Administration National Oceanic and Atmospheric Administration U.S. Department of Agriculture Environmental Protection Agency Consumers play an important role in seafood safety as well. When shopping for seafood, it is important to know what to look for. Read the BLUE FISH HATCHERY Fishwatch Program ‘Seafood & Your Health, page for information about buying, handling, storing, and cooking seafood.</p>


