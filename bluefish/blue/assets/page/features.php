<h2 align="right">Fishes in Farms:</h2>


<img src="../img/feature/h.jpg" hspace="10"  vspace="10"alt="Mountain View" align="left" style="width:500px;height:320px;"><p align="right"> Fish stocking is an important part of any lake management program. The choice of fish to stock depends on the lake owner’s goals and size of the body of water. Large lakes may be hard to manage, but it is also difficult to manage a pond of less than one-acre for species such as bass and bluegill. The most common strategy for pond and lake stocking is to combine largemouth bass and bluegill (and/or redeared sunfish). The bluegill will reproduce and grow rapidly with the abundant food and provide excellent forage for bass. As long as bass are not over-harvested they will keep bluegill from overpopulating. However, some large bluegill will survive bass predation and in turn provide excellent bluegill angling.
</p>
